function draw()
  -- TODO: fix this to use actual screen size
  local sizeX,sizeY = 64,52
  local min_row = 3
  local min_col = 0
  if min_row < 1 then min_row = 1 end
  if min_col < 1 then min_col = 1 end
  local current_color = -1
  while true do
    for frame=1,#nyans do
      for y=1,sizeY do
        for x=1,sizeX do
           local pos = (min_row+y-1 <= 64 and min_col+x-1 <= 64) and string.sub(nyans[frame][min_row+y-1],min_col+x-1,min_col+x-1) or ","
           if _colors[pos] ~= current_color then
            monitor.setBackgroundColor(_colors[pos])
            current_color = _colors[pos]
           end
          monitor.setCursorPos(x*2-2,y-1)
          monitor.write("  ")
        end
      end
      monitor.setCursorPos(1,sizeY)
      monitor.setBackgroundColor(_colors[","])
      current_color = _colors[","]
      monitor.setTextColor(_colors["."])
      monitor.write("                                       This Server has Nyaned for ")
      monitor.write(tostring(math.floor(os.clock())))
      monitor.write(" seconds                            ")
      sleep(0.1)
    end
  end
end

